/*
给定一 N * M 的场地
W代表水洼 W相邻8个方向的W算作一格水洼
计算有多少个水洼
* */


// 输入
const N = 10;
const M = 12;
const field = [
    ["W", ".", ".", ".", ".", ".", ".", ".", ".", "W", "W", ".",],
    [".", "W", "W", "W", ".", ".", ".", ".", ".", "W", "W", "W",],
    [".", ".", ".", ".", "W", "W", ".", ".", ".", "W", "W", ".",],
    [".", ".", ".", ".", ".", ".", ".", ".", ".", "W", "W", ".",],
    [".", ".", ".", ".", ".", ".", ".", ".", ".", "W", ".", ".",],
    [".", ".", "W", ".", ".", ".", ".", ".", ".", "W", ".", ".",],
    [".", "W", ".", "W", ".", ".", ".", ".", ".", "W", "W", ".",],
    ["W", ".", "W", ".", "W", ".", ".", ".", ".", ".", "W", ".",],
    [".", "W", ".", "W", ".", ".", ".", ".", ".", ".", "W", ".",],
    [".", ".", "W", ".", ".", ".", ".", ".", ".", ".", "W", ".",],
];

function dfs(x, y) {
    field[x][y] = ".";
    for (let dx = -1; dx <= 1; dx++) {
        for (let dy = -1; dy <= 1; dy++) {
            const nx = x + dx,
                ny = y + dy;
            if (0 <= nx && nx < N && 0 <= ny && ny < M && field[nx][ny] === 'W') {
                dfs(nx, ny)
            }
        }
    }
}

function solve() {
    let res = 0;
    for (let i = 0; i < N; i++) {
        for (let j = 0; j < M; j++) {
            if (field[i][j] === 'W') {
                dfs(i, j);
                res++
            }
        }
    }

    return res;
}

console.log(solve()); // 3
